
public class Cuestionario {
	private int IDCuestionario;
	private int NumCuestionario;
	private String DescripcionCuestionario;
	public int getIDCuestionario() {
		return IDCuestionario;
	}
	public void setIDCuestionario(int iDCuestionario) {
		IDCuestionario = iDCuestionario;
	}
	public int getNumCuestionario() {
		return NumCuestionario;
	}
	public void setNumCuestionario(int numCuestionario) {
		NumCuestionario = numCuestionario;
	}
	public String getDescripcionCuestionario() {
		return DescripcionCuestionario;
	}
	public void setDescripcionCuestionario(String descripcionCuestionario) {
		DescripcionCuestionario = descripcionCuestionario;
	}
}
